class AveragePCM {
    public static void main(String[] args) {
        int maths = 94;
        int physics = 95;
        int chemistry = 96;

        int total = maths + physics + chemistry;
        double average = total / 3.0; // Use 3.0 to get a decimal result

        System.out.println("Sam's average mark in PCM is " + average);
    }
}
